// 3. LEGACY INTEGRATION API Endpoints
// Expose endpoint for legacy CRM/ERP to POST publications
app.post("/api/legacy/publications", async (req, res) => {
  const { token, processNumber, title, content, subpoenaDate } = req.body as LegacyPublicationInput;

  if (!token) {
    return res.status(401).json({ error: "Token de autorização da API legada é obrigatório." });
  }

  if (!validTokens.includes(token)) {
    return res.status(403).json({ error: "Token inválido ou expirado." });
  }

  if (!processNumber || !content) {
    return res.status(400).json({ error: "ProcessNumber e Content são campos obrigatórios." });
  }

  try {
    // Run AI analysis on the raw legacy text
    const prompt = `Analise o texto recebido de um sistema jurídico legado e retorne uma classificação JSON.
    Siga estritamente esta estrutura JSON:
    {
      "title": "Um resumo amigável do assunto em português",
      "category": "Escolha entre: 'urgente', 'prazo', 'informativo', 'audiencia'",
      "urgencyLevel": "Escolha entre: 'alta', 'media', 'baixa'",
      "deadlineDays": "Prazo em dias (inteiro). Se não identificado, use 0",
      "actionRequired": "Recomendação de providência para o advogado"
    }

    Texto:
    "${content}"`;

    const aiRes = await ai.models.generateContent({
      model: "gemini-3.5-flash",
      contents: prompt,
      config: {
        responseMimeType: "application/json",
      }
    });

    const aiText = aiRes.text;
    const parsed = aiText ? JSON.parse(aiText.trim()) : {};

    const rawDate = subpoenaDate ? new Date(subpoenaDate) : new Date();
    const deadlineDays = parseInt(parsed.deadlineDays) || 0;
    const dueDate = deadlineDays > 0 
      ? calculateBusinessDaysDate(rawDate, deadlineDays)
      : rawDate.toISOString();

    const newLegacyPub = {
      id: `legacy-${Date.now()}-${Math.floor(Math.random() * 1000)}`,
      processNumber: processNumber,
      title: title || parsed.title || "Intimação Recebida via API",
      content: content,
      source: "legacy_api",
      category: parsed.category || "prazo",
      urgencyLevel: parsed.urgencyLevel || "media",
      subpoenaDate: rawDate.toISOString(),
      deadlineDays: deadlineDays,
      dueDate: dueDate,
      actionRequired: parsed.actionRequired || "Verificar publicação integrada via API.",
      status: "pendente",
      createdAt: new Date().toISOString()
    };

    legacyPublicationsPool.unshift(newLegacyPub);

    return res.json({
      success: true,
      message: "Publicação recebida e analisada com IA com sucesso.",
      publication: newLegacyPub
    });

  } catch (error: any) {
    console.error("Erro no processamento da API legada:", error);
    return res.status(500).json({ error: "Falha ao processar postagem da API legada: " + error.message });
  }
});

